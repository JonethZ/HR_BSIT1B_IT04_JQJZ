                    jqjzstrSQL = "SELECT * FROM tbljqjz_departments WHERE Department_id=?";
                    jqjzpreparedStatement = jqjzconn.prepareStatement(jqjzstrSQL);
                    jqjzpreparedStatement.setString(1,strjqjzDepID);

                    jqjzrs = jqjzpreparedStatement.executeQuery();
                    
