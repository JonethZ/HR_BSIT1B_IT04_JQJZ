    private void btnjqjzSave_empMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnjqjzSave_empMouseClicked
        // TODO add your handling code here:
        if(txtjqjzEmpID.getText().length() == 0){
             JOptionPane.showMessageDialog(null,"Please Enter Employee ID!");
             txtjqjzEmpID.requestFocus();
         }
         else if(txtjqjzEmpName.getText().length() == 0){
             JOptionPane.showMessageDialog(null,"Please Enter Employee Name!");
             txtjqjzEmpName.requestFocus();
         }
         else if(txtjqjzEmpAddress.getText().length() == 0){
             JOptionPane.showMessageDialog(null,"Please Enter Employee Address!");
             txtjqjzEmpAddress.requestFocus();
         }
          else if(txtjqjzEmpMobile.getText().length() == 0){
             JOptionPane.showMessageDialog(null,"Please Enter Employee Mobile!");
             txtjqjzEmpMobile.requestFocus();
         }
          else if(txtjqjzEmpEmail.getText().length() == 0){
             JOptionPane.showMessageDialog(null,"Please Enter E-mail Address!");
             txtjqjzEmpEmail.requestFocus();
         }
          else if(pwjqjzEmpPass.getText().length() == 0){
             JOptionPane.showMessageDialog(null,"Please Enter Employee Password!");
             pwjqjzEmpPass.requestFocus();
         }
         else{
             try {
                //intID = Integer.parseInt(txtID.getText());
                strjqjzEmpID = txtjqjzEmpID.getText();
                strjqjzEmpName = txtjqjzEmpName.getText();
                strjqjzEmpAddress = txtjqjzEmpAddress.getText();
                strjqjzEmpMobile = txtjqjzEmpMobile.getText();
                strjqjzEmpEmail = txtjqjzEmpEmail.getText();
                strjqjzEmpPass = pwjqjzEmpPass.getText();
                
                jqjzconn = jqjzC.makeConnection();
                jqjzstrSQL = "SELECT COUNT(*) AS totRec FROM tbljqjz_employees WHERE Employee_id=?";
                jqjzpreparedStatement = jqjzconn.prepareStatement(jqjzstrSQL);
                jqjzpreparedStatement.setString(1,strjqjzEmpID);
                jqjzrs = jqjzpreparedStatement.executeQuery();
                
                while (jqjzrs.next()) {
                    jqjznumberOfRows = jqjzrs.getInt("totRec");
                    //JOptionPane.showMessageDialog(null, numberOfRows);
                }
                if(jqjznumberOfRows == 1){
                    JOptionPane.showMessageDialog(null, "Record Already Exist!");
                }
                else {
                    String jqjzinsertTableSQL = "INSERT INTO tbljqjz_employees"
                    + "(Employee_id, Employee_password, Employee_email, Employee_name, Employee_address, Employee_mobile) VALUES"
                    + "(?,?,?,?,?,?)";
                    jqjzpreparedStatement = jqjzconn.prepareStatement(jqjzinsertTableSQL);
                    jqjzpreparedStatement.setString(1, strjqjzEmpID);
                    jqjzpreparedStatement.setString(2, strjqjzEmpPass);
                    jqjzpreparedStatement.setString(3, strjqjzEmpEmail);
                    jqjzpreparedStatement.setString(4, strjqjzEmpName);
                    jqjzpreparedStatement.setString(5, strjqjzEmpAddress);
                    jqjzpreparedStatement.setString(6, strjqjzEmpMobile);
                    
                    // execute insert SQL stetement
                    jqjzpreparedStatement .executeUpdate();
                    JOptionPane.showMessageDialog(null, "Record Successfully Saved!");
                }
            }
            catch(SQLException jqjzE){
                System.out.println("Failed");
                jqjzE.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnjqjzSave_empMouseClicked

